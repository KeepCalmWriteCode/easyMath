	public static void print2DMatrix(int[][] n) {

		for (int i = 0; i < n.length; i++) {
			System.out.print("{");
			for (int j = 0; j < n[0].length; j++) {
				System.out.print(n[i][j]);
				if (j != n[0].length - 1)
					System.out.print(",");
			}
			System.out.print("}");
			System.out.println();
		}

	}

